<section id="fullbio">
    <h2>Official Bragging Bio</h2>

    <p>
        Originally a professional musician and circus clown, Derek Sivers created CD&nbsp;Baby in 1998.
    </p>

    <p>
        It became the largest seller of independent music online, with <a href="/done">$100M in sales for 150,000
            musicians</a>.
    </p>

    <p>
        In 2008, Derek <a href="/trust">sold CD Baby for $22M</a>, giving the proceeds to a charitable trust for music
        education.
    </p>

    <p>
        He is a frequent <a href="http://www.ted.com/speakers/derek_sivers.html">speaker at the TED Conference</a>, with
        over 5 million views of his talks.
    </p>

    <p>
        Since 2011 he has published 34 books, including “<a href="/a">Anything You Want</a>” which shot to #1 on all of
        its Amazon categories.
    </p>
</section>

